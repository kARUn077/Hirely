export const getAllJobs = async (req, res) => {
  try {
    const keyword = req.query.keyword || "";
    const query = {
      $or: [
        { title: { $regex: keyword, $options: "i" } },
        { description: { $regex: keyword, $options: "i" } },
      ],
    };
    const jobs = await Job.find(query)
      .populate({
        path: "company",
      })
      .sort({ createdAt: -1 });
    if (!jobs || jobs.length === 0) {
      return res.status(200).json({ jobs: [], success: true });

    }
    // ✅ Get user ID from middleware (you must be using verifyToken)
    const userId = req.id;
    const user = await User.findById(userId);

   const bookmarkedJobIds = ((user && user.bookmarkedJobs) || []).map((id) => id.toString());

    const jobsWithBookmark = jobs.map((job) => {
      const jobObj = job.toObject(); // convert Mongoose doc to plain object
      jobObj.isBookmarked = bookmarkedJobIds.includes(job._id.toString());
      return jobObj;
    });

    return res.status(200).json({
      jobs: jobsWithBookmark,
      success: true,
    });
  } catch (error) {
    console.log(error);
    res.status(500).json({
      message: "Internal Server Error",
      success: false,
    });
  }
};

